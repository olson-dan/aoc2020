func processInput() -> [[Bool]] {
    var out = [[Bool]]()
    for (y, line) in day3_input.split(separator: "\n").enumerated() {
        out.append([])
        for char in line {
            out[y].append(char == "#")
        }
    }
    return out
}

func day3_check_step(input: [[Bool]], step_x: Int, step_y: Int) -> Int64
{
    let len_x = input[0].count
    let len_y = input.count
    var x = 0
    var y = 0
    var num_trees = 0
    while y < len_y {
        let row = input[y]
        if row[x % len_x] {
            num_trees += 1
        }
        x += step_x
        y += step_y
    } 
    return Int64(num_trees)
}

func day3_part1() {
    let input = processInput()
    let answer = day3_check_step(input: input, step_x: 3, step_y: 1)
    print("trees: \(answer)")
}

func day3_part2() {
    let input = processInput()
    let a = day3_check_step(input: input, step_x: 1, step_y: 1)
    let b = day3_check_step(input: input, step_x: 3, step_y: 1)
    let c = day3_check_step(input: input, step_x: 5, step_y: 1)
    let d = day3_check_step(input: input, step_x: 7, step_y: 1)
    let e = day3_check_step(input: input, step_x: 1, step_y: 2)
    let answer = a * b * c * d * e
    print("trees: \(answer)")    
}